export function getMonthName1(month) {
    return getMonthName((month - 1) % 12)
}

/**
 * Get localized greeting based on time of day
 * @returns {string}
 */
export function getGreeting() {
    const hour = new Date().getHours()
    const greetings = translations[currentLang]?.dashboard?.greeting || translations.es.dashboard.greeting
    if (hour >= 5 && hour < 12) return greetings.morning
    if (hour >= 12 && hour < 20) return greetings.afternoon
    return greetings.evening
}

