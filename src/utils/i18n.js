const STORAGE_KEY = 'finanzapro_lang'
const DEFAULT_LANG = 'es'
const SUPPORTED_LANGS = ['es', 'en', 'pt']

let currentLang = DEFAULT_LANG

/**
 * Initialize i18n ‚Äî load saved language from localStorage
 */
export function initI18n() {
    const saved = localStorage.getItem(STORAGE_KEY)
    if (saved && SUPPORTED_LANGS.includes(saved)) {
        currentLang = saved
    }
    // Update HTML lang attribute
    document.documentElement.lang = currentLang
}

/**
 * Get translation by dot-notation key
 * @param {string} key - e.g. 'nav.dashboard' or 'months'
 * @param {object} vars - variables for interpolation e.g. { pct: 25 }
 * @returns {string|Array} translated value
 */
export function t(key, vars = {}) {
    const keys = key.split('.')
    let value = translations[currentLang]

    for (const k of keys) {
        if (value === undefined || value === null) return key
        value = value[k]
    }

    if (value === undefined || value === null) return key

    // If it's a string, do variable interpolation
    if (typeof value === 'string') {
        return value.replace(/\{(\w+)\}/g, (_, varName) => {
            return vars[varName] !== undefined ? vars[varName] : `{${varName}}`
        })
    }

    return value
}

